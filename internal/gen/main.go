// Command gen drives the schema-agnostic graphql-opgen generator
// (github.com/trackness/graphql-opgen) over the vendored stash-box SDL, emitting
// genqlient operations and fragments, an operation manifest, and a machine-facing
// catalog. It runs as the first step of `go generate` (see generate.go), ahead of
// genqlient itself, supplying the stash-box-specific Config (see stashboxConfig).
//
// go-stashbox is a READ-ONLY client. genops emits one operation per root field of
// every root type the schema declares; to keep the generated surface query-only,
// the driver compiles genops against a Mutation-stripped VIEW of the vendored
// schema. The vendored schema/ files stay byte-exact on disk — the strip happens
// in a throwaway temp directory staged at generate time (see stageQueryOnly).
// genqlient still validates the generated operations against the FULL vendored
// SDL (a superset), so query-only operations validate fine.
package main

import (
	"flag"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"strings"

	"github.com/lightning-rider-999/go-stashbox/internal/exitcode"
	"github.com/lightning-rider-999/go-stashbox/schema"
	genops "github.com/trackness/graphql-opgen"
)

const header = "# Code generated by genops; DO NOT EDIT.\n\n"

// stashboxConfig is the stash-box-specific genops configuration: the
// schema-agnostic compiler in github.com/trackness/graphql-opgen derives the
// structural surface, and this Config supplies everything particular to stash-box
// — the exit-code vocabulary, the CLI naming irregulars, the audited path-named
// exceptions, and the target package the emitted command table references.
func stashboxConfig() genops.Config {
	return genops.Config{
		ExitCodes: genops.ExitCodeProvider{
			Base:               exitCodeNames(exitcode.Base),
			NotFound:           exitcode.NotFound.Name,
			DestructiveRefused: exitcode.DestructiveRefused.Name,
			JobFailed:          exitcode.JobFailed.Name,
			StillRunning:       exitcode.StillRunning.Name,
			Unconfirmed:        exitcode.Unconfirmed.Name,
		},
		Naming:               stashboxNaming(),
		PathNamedAllowlist:   stashboxPathNamedAllowlist(),
		TargetPackageImport:  "github.com/lightning-rider-999/go-stashbox/stashbox",
		OperationConstSuffix: "_Operation",
		CommandTableDoc: []string{
			"generatedCommands is the full operation table, one spec per stash-box",
			"root field, sorted by OpName. buildRootCommand assembles these into the",
			"cobra tree. Query is the genqlient operation const, so the query text",
			"lives in exactly one place. Every entry is a query: go-stashbox is a",
			"read-only client and the generator is fed a query-only schema view.",
		},
	}
}

// exitCodeNames projects the names out of a slice of taxonomy codes, in order.
func exitCodeNames(codes []exitcode.Code) []string {
	names := make([]string, len(codes))
	for i, c := range codes {
		names[i] = c.Name
	}
	return names
}

// stashboxNaming carries stash-box's entity-vocabulary irregulars: the curated
// set the regular structural rules (find<E>/find<E>s, <entity><verb>, prefix
// groups, trailing-"s" plurals) cannot infer. stash-box has no mutations exposed
// here and no subscriptions, so the verb/plural-mutation/subscription machinery
// is unused; the work is routing the read families — query<E>s, search<E>(s),
// and a handful of irregular find<E> fields — to per-entity CLI groups.
//
// Without these rules the 37 query fields route as follows (verified empirically
// by running genops with an empty NamingRules): every query<E>s and search<E>
// field falls through to the fallback group "misc" (queryPerformers ->
// [misc, query-performers]), findTagOrAlias -> [tag-or-alias, get] and
// findScenesBySceneFingerprints -> [scenes-by-scene-fingerprints, list] mangle
// their multi-word stems, and nothing collides only because everything in misc
// is uniquely kebab-named. The rules below pull each read field under its entity
// group and give the two deprecated search<E> singulars a distinct leaf from
// their search<E>s plurals (which would otherwise collide on [<entity>, search]).
func stashboxNaming() genops.NamingRules {
	return genops.NamingRules{
		// stash-box exposes no mutations here, so the entity-verb and
		// plural-mutation vocabularies are empty. There are no subscriptions.
		PluralEntity:        map[string]string{},
		SingularEntity:      map[string]string{},
		SubscriptionPaths:   map[string][]string{},
		VerbSuffixes:        map[string]string{},
		PluralMutationLeaf:  map[string]string{},
		IrregularPluralNoun: map[string]string{},

		// ExactFinds pins the two irregular find<E> fields whose multi-word stems
		// the structural plural/singular rule mangles. The regular finds
		// (findPerformer -> [performer, get], findDrafts -> [draft, list], etc.)
		// need no entry.
		ExactFinds: map[string][]string{
			// rest "TagOrAlias" has no recognised plural -> default [tag-or-alias, get].
			"findTagOrAlias": {"tag", "get-by-alias"},
			// rest "ScenesBySceneFingerprints" ends in "s" -> default
			// [scenes-by-scene-fingerprints, list].
			"findScenesBySceneFingerprints": {"scene", "list-by-fingerprints"},
		},

		// ExactGroups pins every read field the structural rules leave in the
		// fallback group, routing each under its entity with a verb leaf. This is
		// the bulk of the retune: query<E>s -> [<entity>, query], search<E>s ->
		// [<entity>, search], and the singular long-tail (me, version, config,
		// counts, clusters) to a sensible home.
		ExactGroups: map[string][]string{
			// query<E>s families -> [<entity>, query].
			"queryPerformers":     {"performer", "query"},
			"queryStudios":        {"studio", "query"},
			"queryTags":           {"tag", "query"},
			"queryTagCategories":  {"tag-category", "query"},
			"queryScenes":         {"scene", "query"},
			"querySites":          {"site", "query"},
			"querySiteCategories": {"site-category", "query"},
			"queryEdits":          {"edit", "query"},
			"queryUsers":          {"user", "query"},
			"queryNotifications":  {"notification", "query"},
			"queryModAudits":      {"mod-audit", "query"},

			// query-existing matchers -> [<entity>, query-existing].
			"queryExistingScene":     {"scene", "query-existing"},
			"queryExistingPerformer": {"performer", "query-existing"},

			// search<E>s plurals -> [<entity>, search]; the deprecated search<E>
			// singulars take a distinct "search-one" leaf so they do not collide.
			"searchPerformers": {"performer", "search"},
			"searchPerformer":  {"performer", "search-one"},
			"searchScenes":     {"scene", "search"},
			"searchScene":      {"scene", "search-one"},
			"searchStudio":     {"studio", "search"},
			"searchTag":        {"tag", "search"},

			// Singular long-tail.
			"me":                         {"user", "me"},
			"getConfig":                  {"config", "get"},
			"getUnreadNotificationCount": {"notification", "unread-count"},
			"fingerprintClusters":        {"scene", "fingerprint-clusters"},
			// version is a genuine long-tail singleton; leave it to the fallback
			// group below rather than inventing a one-member group.
		},

		// No prefix-keyed families remain once ExactGroups claims the query<E>s and
		// search<E>s fields, so PrefixGroups is empty.
		PrefixGroups: []genops.PrefixGroupRule{},

		// FallbackGroup catches the long tail no rule claims (here only `version`),
		// as [misc, kebab(field)]. It is never produced by any rule above.
		FallbackGroup: "misc",
	}
}

// stashboxPathNamedAllowlist is the audited set of stash-box object/union/
// interface types genops may emit as path-named inline selections rather than
// canonical fragment types (spike finding A3), each mapped to the reason it is
// exempt from the "no path-named struct" invariant. The set is derived
// EMPIRICALLY from genops.AuditPathNamed / UnlistedPathNamed over the query-only
// schema view (see the build report) — every entry below is a type the generator
// actually flagged, and the list exactly accounts for that flagged set (zero
// UnlistedPathNamed drift).
//
// Four families:
//   - Mixed-selection junction wrappers: an object with object edges but no id,
//     so it is neither a referenceable entity (no Ref) nor a flat value type;
//     genops inlines it (isMixedWrapper).
//   - Union-typed fields, decoded by a __typename-keyed UnmarshalJSON (A5). genops
//     records the union type itself as path-named; its concrete members are
//     audited transitively (they are a mechanical consequence of the union).
//   - A result-wrapper container reached recursively, inlined path-named.
//   - Value-type cycles terminated scalars-only so the fragment DAG stays finite.
func stashboxPathNamedAllowlist() map[string]string {
	return map[string]string{
		// Mixed-selection junction wrappers (object edges, no id).
		"PerformerAppearance": "junction {performer: Performer!, as} — Scene.performers / SceneEdit.added_performers/removed_performers/performers",
		"PerformerStudio":     "junction {studio: Studio!, scene_count} — Performer.studios",
		"URL":                 "junction {url, type(deprecated), site: Site!} — every entity's urls field (no id)",

		// Union-typed fields (__typename-keyed decode, A5).
		"EditTarget":          "union Performer|Scene|Studio|Tag — Edit.target / Edit.merge_sources",
		"EditDetails":         "union PerformerEdit|SceneEdit|StudioEdit|TagEdit — Edit.details / Edit.old_details",
		"DraftData":           "union SceneDraft|PerformerDraft — Draft.data",
		"SceneDraftStudio":    "union Studio|DraftEntity — SceneDraft.studio",
		"SceneDraftPerformer": "union Performer|DraftEntity — SceneDraft.performers",
		"SceneDraftTag":       "union Tag|DraftEntity — SceneDraft.tags",

		// Result-wrapper container inlined path-named. Studio.sub_studios:
		// QueryStudiosResultType! is a result wrapper (no id, object edges) whose
		// `studios` edge cycles back to Studio; genops inlines it (count + studios
		// terminated at StudioRef). (Studio.performers: QueryPerformersResultType!
		// takes a REQUIRED input arg, so it is unselectable and never emitted —
		// only QueryStudiosResultType is flagged.)
		"QueryStudiosResultType": "result wrapper {count, studios:[Studio!]} — Studio.sub_studios, inlined path-named (Studio cycle)",

		// Value-type cycles terminated scalars-only (B6). Edit cycles back to
		// Performer/Scene/Tag through the EditTarget union, so Performer.edits /
		// Scene.edits / Tag.edits terminate Edit scalars-only (the patched genops
		// building-stack case); the Edit.comments/votes value graph terminates with it.
		"Edit":        "value-type cycle via EditTarget union back to Performer/Scene/Tag — *.edits terminated scalars-only",
		"EditComment": "value-type cycle Edit.comments -> EditComment.edit: Edit terminated scalars-only",
		"EditVote":    "value-type cycle Edit.votes -> EditVote (-> User) terminated within the Edit cycle",
	}
}

func main() {
	schemaDir := flag.String("schema", "schema", "vendored SDL directory")
	overlayPath := flag.String("overlay", "internal/gen/overlay.yaml", "curated overlay file")
	flag.Parse()

	if err := run(*schemaDir, *overlayPath); err != nil {
		_, _ = fmt.Fprintln(os.Stderr, "genops:", err)
		os.Exit(1)
	}
}

func run(schemaDir, overlayPath string) error {
	cfg := stashboxConfig()

	// READ-ONLY: compile genops against a Mutation-stripped copy of the vendored
	// SDL so it emits query operations only. The vendored files are untouched.
	stagedDir, cleanup, err := stageQueryOnly(schemaDir)
	if err != nil {
		return err
	}
	defer cleanup()

	c, err := genops.Compile(stagedDir, overlayPath, schema.SchemaVersion, cfg)
	if err != nil {
		return err
	}

	// Backstop for the Mutation strip (stageQueryOnly / stripMutation): assert the
	// compiled surface is query-only. ManifestEntry.Kind is "query" | "mutation" |
	// "subscription"; any non-query op means the strip leaked, so fail the build
	// loudly here rather than emit a mutation operation into a read-only client.
	for _, op := range c.Manifest.Operations {
		if op.Kind != "query" {
			return fmt.Errorf("query-only invariant violated: operation %s has kind %q "+
				"(expected query only — the Mutation strip in stageQueryOnly leaked)", op.Name, op.Kind)
		}
	}

	manifestJSON, err := c.Manifest.JSON()
	if err != nil {
		return err
	}
	catalogJSON, err := c.Catalog.JSON()
	if err != nil {
		return err
	}
	commandsGo, err := genops.EmitCommands(c.Manifest, cfg)
	if err != nil {
		return err
	}

	for _, w := range []struct {
		path    string
		content []byte
	}{
		{"internal/gen/generated/fragments.graphql", []byte(header + c.Fragments)},
		{"internal/gen/generated/operations.graphql", []byte(header + c.Operations)},
		{"internal/gen/generated/manifest.json", manifestJSON},
		{"schema/catalog.json", catalogJSON},
		// A byte-identical copy lives beside the CLI so `stashbox catalog` can
		// embed it with go:embed (which cannot reach `..` paths). The check
		// gate diffs both against a fresh `task generate` to keep them in sync.
		{"cmd/stashbox/catalog.json", catalogJSON},
		{"cmd/stashbox/gen_commands.go", commandsGo},
	} {
		if err := writeFile(w.path, w.content); err != nil {
			return err
		}
	}
	return nil
}

// stageQueryOnly copies the vendored SDL into a throwaway temp directory and
// rewrites the staged root schema.graphql to remove the Mutation root type, so
// genops — which emits one operation per root field of every root type the schema
// declares — produces a query-only surface. The vendored files on disk are never
// touched. It mirrors genops' own file discovery (genops.LoadSchema walks *.graphql
// recursively: the root schema.graphql plus everything under types/), so the
// staged tree is a faithful, Mutation-stripped view of what Compile would load.
//
// The returned cleanup removes the temp tree; call it (deferred) regardless of
// the error.
func stageQueryOnly(schemaDir string) (dir string, cleanup func(), err error) {
	tmp, err := os.MkdirTemp("", "stashbox-schema-*")
	if err != nil {
		return "", func() {}, fmt.Errorf("stage schema: %w", err)
	}
	cleanup = func() { _ = os.RemoveAll(tmp) }

	// Walk the vendored SDL exactly as genops.LoadSchema does (every *.graphql,
	// recursively) and reproduce the tree under tmp, stripping Mutation from the
	// root schema.graphql en route.
	err = filepath.WalkDir(schemaDir, func(path string, d fs.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		if d.IsDir() || filepath.Ext(path) != ".graphql" {
			return nil
		}
		rel, relErr := filepath.Rel(schemaDir, path)
		if relErr != nil {
			return relErr
		}
		b, readErr := os.ReadFile(path)
		if readErr != nil {
			return readErr
		}
		// The root schema.graphql carries the Mutation type and the schema{} block;
		// the types/ files do not. Strip Mutation only from the root file.
		if rel == "schema.graphql" {
			b = []byte(stripMutation(string(b)))
		}
		dst := filepath.Join(tmp, rel)
		if mkErr := os.MkdirAll(filepath.Dir(dst), 0o755); mkErr != nil {
			return mkErr
		}
		return os.WriteFile(dst, b, 0o644)
	})
	if err != nil {
		return "", cleanup, fmt.Errorf("stage schema: %w", err)
	}
	return tmp, cleanup, nil
}

// stripMutation removes the Mutation root type from a stash-box root SDL: the
// entire `type Mutation { ... }` block and the `mutation: <Type>` root-operation
// declaration of the `schema { ... }` block. Other type/schema content is
// preserved verbatim, so the staged file is byte-for-byte the vendored file minus
// the Mutation root.
//
// Block closer detection is BLOCK-STRING-AWARE rather than relying on "GraphQL
// type blocks never nest braces". A field description is a triple-quoted `"""..."""`
// string, and a multi-line description can contain a line that is a bare `}` — which
// is NOT the type's closing brace. We track triple-quote open/close state while
// scanning (counting `"""` occurrences per line, so a single-line `"""..."""` that
// opens and closes on one line nets to no state change) and treat only the first
// bare `}` line encountered while NOT inside a block string as the closer. The
// current vendored SDL happens to use only single-line descriptions, so this is
// behaviour-preserving today; it defends against a future SDL that wraps one.
//
// The `mutation: <Type>` root-operation removal handles both layouts: a full-line
// declaration inside a multi-line `schema { ... }` block, and a single-line
// `schema { query: Query mutation: Mutation }` (the `mutation: <Type>` token is
// stripped from the one-line block, leaving the rest intact).
//
// The run() guard is the backstop: it asserts the compiled surface is query-only,
// so any future strip failure becomes a loud build error rather than a silent
// mutation leak into this read-only client.
func stripMutation(sdl string) string {
	lines := strings.Split(sdl, "\n")
	out := make([]string, 0, len(lines))
	inMutation := false
	inBlockString := false
	for _, line := range lines {
		// Update block-string state for this line. An odd number of `"""` on a
		// line flips the state (open or close); an even number (0, or a single-line
		// `"""..."""`) leaves it unchanged. wasInBlockString reflects the state at
		// the START of the line, which is what decides whether a bare `}` closes.
		wasInBlockString := inBlockString
		if strings.Count(line, `"""`)%2 == 1 {
			inBlockString = !inBlockString
		}

		if !inMutation && !wasInBlockString && isTypeMutationOpener(line) {
			inMutation = true
			continue
		}
		if inMutation {
			// The first bare `}` line (trimmed) encountered while NOT inside a block
			// string closes the type block.
			if !wasInBlockString && strings.TrimSpace(line) == "}" {
				inMutation = false
			}
			continue
		}
		// Drop the mutation root-operation declaration from the schema{} block.
		if !wasInBlockString {
			if trimmed := strings.TrimSpace(line); trimmed == "mutation: Mutation" {
				// Full-line declaration inside a multi-line schema{} block.
				continue
			} else if isSingleLineSchemaBlock(trimmed) {
				// Single-line `schema { query: Query mutation: Mutation }`: drop just
				// the `mutation: <Type>` token, keep the rest of the block.
				if stripped, ok := stripMutationRootOp(line); ok {
					out = append(out, stripped)
					continue
				}
			}
		}
		out = append(out, line)
	}
	return strings.Join(out, "\n")
}

// isTypeMutationOpener reports whether a line opens the Mutation root type
// definition, i.e. (trimmed) it is `type Mutation {` or `type Mutation`. A
// description line or a field merely mentioning Mutation does not match; callers
// must additionally confirm the line is not inside a `"""..."""` block string,
// since stripMutation tracks that state separately.
func isTypeMutationOpener(line string) bool {
	t := strings.TrimSpace(line)
	return t == "type Mutation {" || t == "type Mutation"
}

// isSingleLineSchemaBlock reports whether a trimmed line is a one-line schema
// root-operations block, i.e. it begins with `schema` and carries both the
// opening and closing brace on the same line (`schema { ... }`).
func isSingleLineSchemaBlock(trimmed string) bool {
	return strings.HasPrefix(trimmed, "schema") &&
		strings.Contains(trimmed, "{") && strings.Contains(trimmed, "}")
}

// stripMutationRootOp removes a `mutation: <Type>` root-operation token from a
// single-line schema block, collapsing the surrounding whitespace so the rest of
// the block (query:/subscription: operations and braces) is preserved. It reports
// whether a token was found and removed.
func stripMutationRootOp(line string) (string, bool) {
	idx := strings.Index(line, "mutation:")
	if idx < 0 {
		return line, false
	}
	rest := line[idx+len("mutation:"):]
	// Skip the leading space(s), the type name, then trailing space(s), so the
	// surrounding tokens close up cleanly.
	rest = strings.TrimLeft(rest, " \t")
	if cut := strings.IndexAny(rest, " \t}"); cut >= 0 {
		rest = rest[cut:]
	} else {
		rest = ""
	}
	before := strings.TrimRight(line[:idx], " \t")
	// Re-join: if what follows starts with a brace it abuts `before` with a space;
	// otherwise keep a single separating space when both sides are non-empty.
	rest = strings.TrimLeft(rest, " \t")
	switch {
	case before == "" && rest == "":
		return "", true
	case before == "":
		return rest, true
	case rest == "":
		return before, true
	default:
		return before + " " + rest, true
	}
}

func writeFile(path string, b []byte) error {
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, b, 0o644)
}
